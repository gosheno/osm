def infer_spb_district(
    *,
    latitude: float | None,
    longitude: float | None,
    address_text: str | None = None,
) -> str | None:
    normalized_text = (address_text or "").lower().replace("ё", "е")

    for marker, district in STREET_DISTRICT_HINTS:
        if marker in normalized_text:
            return district

    if latitude is None or longitude is None:
        return None

    for district, min_lat, max_lat, min_lon, max_lon in DISTRICT_BOUNDS:
        if min_lat <= latitude <= max_lat and min_lon <= longitude <= max_lon:
            return district

    return None
